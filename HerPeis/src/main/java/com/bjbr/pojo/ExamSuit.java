package com.bjbr.pojo;

public class ExamSuit {
	
	private Integer examSuitId;
	private String examSuitName;
	private double price;
	private boolean sex;
	
	
	public Integer getExamSuitId() {
		return examSuitId;
	}
	public void setExamSuitId(Integer examSuitId) {
		this.examSuitId = examSuitId;
	}
	public String getExamSuitName() {
		return examSuitName;
	}
	public void setExamSuitName(String examSuitName) {
		this.examSuitName = examSuitName;
	}
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	public boolean isSex() {
		return sex;
	}
	public void setSex(boolean sex) {
		this.sex = sex;
	}
	
	

}
